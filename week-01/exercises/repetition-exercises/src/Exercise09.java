public class Exercise09
{
    public static void main(String[] args)
    {
        // 1. Write a loop to sum all numbers between 7 and 16.
        int num = 0;
        for (int x = 7; x <= 16; x ++)
            num += x;

        // 2. Print the result.
        System.out.print(num);
    }
}
